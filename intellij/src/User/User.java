package User;

public class User {

    private String nome;
    private boolean colore;

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public boolean isColore() {
        return colore;
    }

    public void setColore(boolean colore) {
        this.colore = colore;
    }


}
